# Class defining a rudder
class Rudder:
    # Method to align the rudder
    def align(self):
        print("Rudder is aligned")

    # Method to turn the rudder to the left
    def turnLeft(self):
        print("Rudder is turned left")

    # Method to turn the rudder to the right
    def turnRight(self):
        print("Rudder is turned right")